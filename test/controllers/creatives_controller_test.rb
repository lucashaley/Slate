require "test_helper"

class CreativesControllerTest < ActionDispatch::IntegrationTest
  setup do
    @creative = creatives(:one)
  end

  test "should get index" do
    get creatives_url
    assert_response :success
  end

  test "should get new" do
    get new_creative_url
    assert_response :success
  end

  test "should create creative" do
    assert_difference("Creative.count") do
      post creatives_url, params: { creative: { pitch_id: @creative.pitch_id, position: @creative.position, user_id: @creative.user_id } }
    end

    assert_redirected_to creative_url(Creative.last)
  end

  test "should show creative" do
    get creative_url(@creative)
    assert_response :success
  end

  test "should get edit" do
    get edit_creative_url(@creative)
    assert_response :success
  end

  test "should update creative" do
    patch creative_url(@creative), params: { creative: { pitch_id: @creative.pitch_id, position: @creative.position, user_id: @creative.user_id } }
    assert_redirected_to creative_url(@creative)
  end

  test "should destroy creative" do
    assert_difference("Creative.count", -1) do
      delete creative_url(@creative)
    end

    assert_redirected_to creatives_url
  end
end
